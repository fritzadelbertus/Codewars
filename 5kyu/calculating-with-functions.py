def zero(n = 0):  return 0 if n == 0 else int(eval(f'0{n}'))
def one(n = 0):   return 1 if n == 0 else int(eval(f'1{n}'))
def two(n = 0):   return 2 if n == 0 else int(eval(f'2{n}'))
def three(n = 0): return 3 if n == 0 else int(eval(f'3{n}'))
def four(n = 0):  return 4 if n == 0 else int(eval(f'4{n}'))
def five(n = 0):  return 5 if n == 0 else int(eval(f'5{n}'))
def six(n = 0):   return 6 if n == 0 else int(eval(f'6{n}'))
def seven(n = 0): return 7 if n == 0 else int(eval(f'7{n}'))
def eight(n = 0): return 8 if n == 0 else int(eval(f'8{n}'))
def nine(n = 0):  return 9 if n == 0 else int(eval(f'9{n}'))

def plus(n):       return f'+{n}'
def minus(n):      return f'-{n}'
def times(n):      return f'*{n}'
def divided_by(n): return f'/{n}'
